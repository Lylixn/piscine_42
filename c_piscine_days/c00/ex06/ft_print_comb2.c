#include <unistd.h>

void	print_double(int n)
{
	int	n1;
	int	n2;

	n1 = n / 10 + 48;
	n2 = n % 10 + 48;
	write(1, &n1, 1);
	write(1, &n2, 1);
}

void	ft_print_comb2(void)
{
	int	n[2];

	n[0] = 0;
	n[1] = 1;
	while (n[0] <= 98)
	{
		while (n[1] <= 99)
		{
			print_double(n[0]);
			write(1, " ", 1);
			print_double(n[1]);
			if (n[0] != 98)
			{
				write(1, ",", 1);
				write(1, " ", 1);
			}
			n[1]++;
		}
		n[0]++;
		n[1] = n[0] + 1;
	}
}
