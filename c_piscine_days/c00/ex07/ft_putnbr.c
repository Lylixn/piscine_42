#include <unistd.h>

void	print(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else
	{
		if (nb < 0)
		{
			print('-');
			nb *= -1;
		}
		while (nb > 9)
		{
			ft_putnbr(nb / 10);
			nb %= 10;
		}
		print(nb + 48);
	}
	write(1, "\n", 1);
}