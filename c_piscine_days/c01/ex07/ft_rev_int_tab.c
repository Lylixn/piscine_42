void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	swap;

	i = -1;
	swap = 0;
	while (i++ < (size - 1) / 2)
	{
		swap = tab[size - i - 1];
		tab[size - i - 1] = tab[i];
		tab[i] = swap;
	}
}
